/**
 * Fitxer central de contingut normatiu per a tooltips pedagògics.
 *
 * Agrega i reexporta totes les dades normatives (CE, EAC, LO, STC)
 * del sistema per facilitar l'accés centralitzat.
 *
 * Per a ús dels tooltips en línia: veure utils/normativeDataResolver.js
 * Per a ús de les capçaleres per punt: veure data/bloc1Tema{1,2,3}Normativa.js
 */

import { CEArticles } from './constitucioCE.js';
import { EACArticles } from './eac2006.js';
import { LOData } from './lleisOrganiques.js';
import { jurisprudenciaTC } from './jurisprudenciaTC.js';

const buildNormativaKey = (type, reference) => {
  if (type === 'CE') return `Art.${reference} CE`;
  if (type === 'EAC') return `Art.${reference} EAC`;
  if (type === 'LO') return `LO ${reference}`;
  return `STC ${reference}`;
};

const buildNormativaTitle = (type, reference) => {
  if (type === 'CE') return `Article ${reference} Constitució Espanyola`;
  if (type === 'EAC') return `Article ${reference} Estatut d'Autonomia de Catalunya`;
  if (type === 'LO') return `LO ${reference} (llei orgànica)`;
  return `STC ${reference} (Tribunal Constitucional)`;
};

const mapLegacyEntry = (type, reference, entry = {}) => ({
  title: entry.titol || buildNormativaTitle(type, reference),
  summary: entry.queRegula || '',
  keyIdea: entry.ideaClau || '',
  examNote: entry.clauExamen || '',
  contextTemari: entry.contextTemari || '',
  errorHabitual: entry.errorHabitual || '',
});

const normalizeDataset = (dataset, type) => Object.fromEntries(
  Object.entries(dataset || {}).map(([reference, entry]) => ([
    buildNormativaKey(type, reference),
    mapLegacyEntry(type, reference, entry),
  ])),
);

const sortEntries = (entries) => Object.fromEntries(
  Object.entries(entries).sort(([first], [second]) => first.localeCompare(second, 'ca')),
);

const baseNormativaContent = {
  ...normalizeDataset(CEArticles, 'CE'),
  ...normalizeDataset(EACArticles, 'EAC'),
  ...normalizeDataset(LOData, 'LO'),
  ...normalizeDataset(jurisprudenciaTC, 'STC'),
};

